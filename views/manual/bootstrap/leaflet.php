  * 작업화면이 작아서 화면 미리보기가 밑으로 떨어질 경우, 좌메뉴 상단 <span>[X]버튼</span>을 클릭하시면 메뉴가 일시적으로 닫히며 다시 열고자 할때는 지니 상단로고 왼쪽 <span>[전체메뉴]버튼</span>을 클릭하시면 됩니다.<br />
  * 대표전화 : 1522-7985 (지니 사용시 궁금하신 내용이나 템플릿 추가요청을 원하실 경우 대표전화로 연락주시면 됩니다.)
