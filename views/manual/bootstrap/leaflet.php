  </p>
  <video width="1200" controls>
    <source src="/dhn/movie/2020-11-30 14-18-31.mp4" type="video/mp4">
  </video>
  <ol>
    <li>1. [행사코너 순서변경] 버튼 클릭시 원하시는데로 행사코너 순서변경이 가능합니다.</li>
    <li>2. 버튼은 왼쪽부터 순서대로 [처음으로 이동/왼쪽으로 이동/오른쪽으로 이동/마지막으로 이동]이며 적용하기 버튼을 클릭하셔야 합니다.</li>
  </ol>

