									<dt>Q2. 안심번호로도 발송 가능한가요?</dt>
									<dd>안심번호는 안심번호 서비스사에서 본래 전화번호에 임시로 연결한 전화번호로서 그 연결 현황은 해당 서비
									스사에서만 확인이 가능합니다. 따라서 카카오톡 비즈메시지 발송에 사용할 수 없습니다.
									</dd>
								</dl>
								<dl>
									<dt>Q3. 알림톡으로 사내 알림 문자를 보낼 수는 없나요?</dt>
									<dd>알림톡은 장애 발생 알림, 결재 요청 알림 등 사내 직원 대상의 업무상 이벤트 자동알림용으로 활용이 가능합
									니다. 다만 업무와 관련되지 않거나 행사 안내, 공지사항 전파, 뉴스레터류의 구독형 메시지 등 비정형화된 메
									시지는 알림톡 서비스 대상이 아닙니다(알림톡으로 소화 불가능한 사내용 메시지는 친구톡 사용을 권장드립
									니다). 기존 문자 발송 내용으로 알림톡 사용이 가능한지에 대해서는 공식딜러사 또는
									help.bzm@kakaocorp.com로 문의주시기 바랍니다.</dd>
								</dl>
								<dl>
									<dt>Q4. 친구톡이나 알림톡은 카카오톡 PC버전에서도 확인이 가능한가요?</dt>
									<dd>친구톡과 알림톡은 카카오톡 PC버전에서도 확인이 가능합니다. 다만 알림톡의 경우 ‘메시지가 도착했습니다.
									모바일에서 확인해 주세요.’와 같은 내용으로 도착 알림만 지원됩니다.</dd>
								</dl>
								<dl>
									<dt>Q5. 알림톡 발송율은 어느 정도인가요?</dt>
									<dd>알림톡은 발송 요청 중 안심번호의 비중, 수신 고객의 연령대 분포에 따라 수신율이 다소 차이가 있을 수 있습
									니다. 하지만 국내에서만 4,006만 명의 활성 사용자를 보유한 카카오톡을 기반으로 하는 만큼 기대 이상의
									발송율이라는 평가가 일반적입니다.</dd>
								</dl>
								<dl>
									<dt>Q6. API 규격서를 우선 살펴볼 수 있을까요?</dt>
									<dd>카카오톡 비즈메시지 API 규격서는 계약 체결 고객사에 한정하여 제공하고 있습니다. 기본적인 기술적 참고
									사항에 대해 안내해 드리자면 아래와 같습니다.
									• 접근 권한 : ACL에 IP 등록
									• 통신 프로토콜 : https 사용
									• 통신 데이터 형식 : json 사용
									• 통신 방식 : GET, POST method (POST로 발송 요청, GET으로 발송 결과 확인) </dd>
								</dl>
								<dl>
									<dt>Q7. 이미 플러스친구를 운영중입니다. 이 프로필로 알림톡을 발송할 수 있나요?</dt>
									<dd>네, 운영중인 플러스친구 프로필을 비즈메시지 센터에 발신프로필로 등록하여 알림톡을 발송할 수 있습니다.</dd>
								</dl>
								<dl>
									<dt>Q8. 플러스친구에는 기본적으로 1:1 채팅 기능이 있는데 상담톡이 따로 필요한 이유?</dt>
									<dd>플러스친구는 1:1 채팅 기능을 무료로 제공하고 있습니다. 플러스친구 관리자 웹/앱을 통해 카카오톡을 쓰듯 플러
									스친구와 친구 맺은 이용자들과 채팅이 가능합니다. 단, 수십에서 수천 명 단위의 상담원이 상주하는 대형 고객센
									터에서 본격적인 상담 채널로 활용하려면 더 많은 기능이 뒷받침된 맞춤형 서비스로 탈바꿈해야 합니다. 상담톡은
									바로 그런 기회를 제공합니다. 그 외에도 카톡 상담하기 버튼 API, 친구 추가 없는 상담 프로세스와 같은 플러스친
